"""
type conversion / type casting

to convert from 1 data type to another
str -> int

"""

# a = "soni"
# b = "aryan"
# print(a + b)

a = "50"
b = "4"
print(a + b)

c = int(a)
d = int(b)

print(c + d)


# str - float

a = float("34.56")
b = float("39.94")
print(a)
print(b)
print(a + b)


# float -> int

a = int(55.25)
print(a)
c = int(44.32)
print(c)
